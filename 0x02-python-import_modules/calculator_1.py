#!/usr/bin/python3
def add(a, b):
    """
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """
        The return value. a / b
    """
    return int(a / b)
